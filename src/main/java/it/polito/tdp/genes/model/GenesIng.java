package it.polito.tdp.genes.model;

public class GenesIng {
	
	public Genes gene;
	public int ing;
	
	public GenesIng(Genes gene, int ing) {
		super();
		this.gene = gene;
		this.ing = ing;
	}

	public Genes getGene() {
		return gene;
	}

	public void setGene(Genes gene) {
		this.gene = gene;
	}

	public int getIng() {
		return ing;
	}

	public void setIng(int ing) {
		this.ing = ing;
	}
	
	

}
