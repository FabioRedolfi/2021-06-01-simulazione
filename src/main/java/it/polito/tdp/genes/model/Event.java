package it.polito.tdp.genes.model;

public class Event {
	
	public int month;
	public int numIng;
	
	public Event(int month, int numIng) {
		super();
		this.month = month;
		this.numIng = numIng;
	}
	
	public int getMonth() {
		return month;
	}
	
	public void setMonth(int month) {
		this.month = month;
	}
	
	public int getNumIng() {
		return numIng;
	}
	
	public void setNumIng(int numIng) {
		this.numIng = numIng;
	}
	
	
	
	
	
	
}
